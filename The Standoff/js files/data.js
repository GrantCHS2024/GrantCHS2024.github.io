let activeSounds = [];
function loadAudio(src){
   const aud = new Audio();
    aud.src = src;
    return aud;
}
function playSound(sound){
   const sfx = sound.cloneNode();
   sfx.play();

   activeSounds.push(sfx);

   sfx.onended = () => {
    const index = activeSounds.indexOf(sfx);
    if (index > -1) {
      activeSounds.splice(index, 1);
    }
  };
}
function clearSounds() {
  activeSounds.forEach(sound => {
    sound.pause();
    sound.currentTime = 0;
  });

  activeSounds.length = 0;
}

function adjustAmbienceVolume(){
   for(var key in backgroundAudio){
      backgroundAudio[key].volume = $("#ambienceAudio").val();
   }
}

